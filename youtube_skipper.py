def is_color_close_to_white(color):
    # Definujte prah pre blízkosť k bielej farbe
    threshold = 130
    # Farba je blízka k bielej, ak je každá zložka (R, G, B) väčšia ako prah
    return all(value >= threshold for value in color)

def compare_arrays(array1, array2, threshold=0.91):
    # Počet zhody v poliach
    matching_count = sum(1 for a, b in zip(array1, array2) if a == b)
    # Pomer zhody k celkovému počtu prvkov v poliach
    match_ratio = matching_count / len(array1)        
    return match_ratio >= threshold

